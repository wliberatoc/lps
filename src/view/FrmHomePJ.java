    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // excluir
        int confirma = 0;
        confirma = JOptionPane.showConfirmDialog(rootPane, "Confirma que deseja EXCLUIR SEU CADASTROS?","Confima√ß√£o",JOptionPane.YES_NO_OPTION);
        if(confirma == JOptionPane.YES_OPTION){
            if(pj.delete(cliente.get(0).getId()) && contaPJ.deleteAll(cliente.get(0).getCnpj())){
                JOptionPane.showMessageDialog(rootPane, "Conta Excluida");
                new FrmLogin().setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Erro ao tentar Excluir");
            }     
        }
            
    }//GEN-LAST:event_btnExcluirActionPerformed

